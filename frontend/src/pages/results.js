/**
 * Results Page – src/pages/results.js
 * Polls /api/search/results/{sessionId} until complete, then renders results.
 */
import React, { useState, useEffect, useRef, useCallback } from "react"
import { Link, navigate } from "gatsby"
import config from "../config"
import { addJobId } from "../utils/session"
import SearchHistory from "../components/search/SearchHistory"
import ResultsView from "../components/search/ResultsView"
import Container from "../components/common/Container"
import { LOADING_PROMPTS } from "../components/common/TerminalLoader"
import { formatSeq } from "../utils/lib"

const GITHUB_REPO = "ababaian/petadex.io"

const SEARCH_STAGES = [
  { id: "submit", label: "Query submitted", pct: 15 },
  { id: "index", label: "Checking for cached result", pct: 35 },
  { id: "align", label: "Running MMseqs2 search", pct: 65 },
  { id: "enrich", label: "Enriching with families", pct: 85 },
  { id: "done", label: "Results ready", pct: 100 },
]

function buildBugReportUrl(message, jobId) {
  const title = encodeURIComponent(`[Bug] Search error: ${message}`)
  const body = encodeURIComponent(
    `## Automatic error report\n\n**Job ID:** \`${jobId || "N/A"}\`\n` +
    `**URL:** ${typeof window !== "undefined" ? window.location.href : "N/A"}\n` +
    `**Timestamp:** ${new Date().toISOString()}\n**Error:** ${message}\n\n` +
    `*Auto-generated by PETadex error handler*`
  )
  return `https://github.com/${GITHUB_REPO}/issues/new?title=${title}&body=${body}&labels=bug`
}

function transformResults(rawResults, queryLength) {
  return (rawResults || []).map((hit, index) => ({
    rank: index + 1,
    accession: hit.target_id || hit.accession,
    target_id: hit.target_id || hit.accession,
    name: hit.target_name || hit.name || hit.metadata?.definition || null,
    organism: hit.organism || hit.metadata?.organism || null,
    taxonomy: hit.taxonomy || hit.metadata?.taxonomy || null,
    percent_identity: hit.percent_identity ?? hit.identity,
    identity: hit.percent_identity ?? hit.identity,
    evalue: hit.evalue,
    bitscore: hit.bitscore,
    query_coverage:
      queryLength && hit.query_end != null && hit.query_start != null
        ? Math.round(((hit.query_end - hit.query_start + 1) / queryLength) * 100)
        : (hit.query_coverage ?? null),
    alignment_length: hit.alignment_length,
    query_start: hit.query_start,
    query_end: hit.query_end,
    target_start: hit.target_start,
    target_end: hit.target_end,
    enzyme_id: hit.enzyme_id ?? null,
    family: hit.family ?? null,
    component: hit.component ?? null,
    family_pid: hit.family_pid ?? null,
    has_tree: hit.has_tree ?? false,
  }))
}

// ─── Loading Screen ────────────────────────────────────────────────────────

const LoadingScreen = ({ sessionId, sequence, elapsed }) => {
  const [stageIdx, setStageIdx] = useState(0)
  const [barPct, setBarPct] = useState(5)
  const [promptIdx, setPromptIdx] = useState(() => Math.floor(Math.random() * LOADING_PROMPTS.length))

  useEffect(() => {
    const interval = setInterval(() => {
      setPromptIdx(i => (i + 1) % LOADING_PROMPTS.length)
    }, 2500)
    return () => clearInterval(interval)
  }, [])

  useEffect(() => {
    const thresholds = [0, 3, 8, 15, 30]
    let next = 0
    for (let i = 0; i < thresholds.length; i++) {
      if (elapsed >= thresholds[i]) next = i
    }
    const clampedStage = Math.min(next, SEARCH_STAGES.length - 2)
    setStageIdx(clampedStage)
    const start = SEARCH_STAGES[clampedStage].pct
    const end = SEARCH_STAGES[Math.min(clampedStage + 1, SEARCH_STAGES.length - 1)].pct
    const progress = elapsed - thresholds[clampedStage]
    const span = (thresholds[clampedStage + 1] || 60) - thresholds[clampedStage]
    setBarPct(Math.round(start + (end - start) * Math.min(progress / span, 0.9)))
  }, [elapsed])

  const cleanSeq = (sequence || "")
    .replace(/^>.*\n?/gm, "")
    .replace(/[\s\r]/g, "")
    .toUpperCase()

  return (
    <div className="py-10 px-6 flex flex-col items-center gap-6 max-w-lg mx-auto">
      {/* Header */}
      <div className="flex flex-col items-center gap-3 text-center">
        <div className="w-10 h-10 rounded-full border border-t-accent animate-spin" />
        <h2 className="text-xl font-semibold text-primary">
          Searching the enzyme database…
        </h2>
        <p className="text-sm text-secondary-foreground">
          MMseqs2 is scanning millions of sequences for similar enzymes
        </p>
        <p className="text-xs text-muted-foreground font-mono">› {LOADING_PROMPTS[promptIdx]}</p>
      </div>

      {/* Progress bar */}
      <div className="w-full h-2 bg-surface-sunken rounded-full overflow-hidden">
        <div
          className="h-full bg-accent rounded-full transition-all duration-500"
          style={{ width: `${barPct}%` }}
        />
      </div>

      {/* Stage list */}
      <ul className="w-full space-y-2">
        {SEARCH_STAGES.slice(0, -1).map((stage, i) => {
          const done = i < stageIdx
          const active = i === stageIdx
          return (
            <li key={stage.id} className="flex items-center gap-3">
              <span
                className={[
                  "w-6 h-6 rounded-full flex items-center justify-center text-xs font-bold shrink-0 transition-colors",
                  done ? "bg-success text-white"
                    : active ? "bg-info text-white ring-2 ring-info/30"
                      : "bg-border text-muted-foreground",
                ].join(" ")}
              >
                {done ? "✓" : active ? "…" : ""}
              </span>
              <span
                className={[
                  "text-sm transition-colors",
                  done ? "text-success font-medium"
                    : active ? "text-info font-semibold"
                      : "text-muted-foreground",
                ].join(" ")}
              >
                {stage.label}
              </span>
            </li>
          )
        })}
      </ul>

      {/* Query sequence */}
      {cleanSeq && (
        <div className="w-full bg-surface-sunken border rounded-lg p-3">
          <span className="label block mb-1">Query sequence ({cleanSeq.length} aa)</span>
          <span className="sequence text-2xs">{formatSeq(cleanSeq)}</span>
        </div>
      )}

      {/* Meta chips */}
      <div className="flex flex-wrap gap-2 justify-center">
        <span className="badge badge-brand">⏱ {elapsed}s elapsed</span>
        {sessionId && (
          <span
            className="badge badge-brand"
            title="Share this ID to reload your results later"
          >
            🔑 {sessionId.slice(0, 12)}…
          </span>
        )}
      </div>
    </div>
  )
}

// ─── Error View ────────────────────────────────────────────────────────────

const ErrorView = ({ error, onNewSearch }) => (
  <div className="py-10 px-6 flex flex-col items-center gap-4 text-center max-w-lg mx-auto">
    <h2 className="text-xl font-semibold text-destructive">Search failed</h2>
    <p className="text-sm text-secondary-foreground">{error.message}</p>
    {error.bugUrl && (
      <a
        href={error.bugUrl}
        target="_blank"
        rel="noopener noreferrer"
        className="text-sm text-accent hover:text-accent-hover underline underline-offset-2"
      >
        🐛 Report this bug on GitHub
      </a>
    )}
    <button className="btn btn-secondary mt-2" onClick={onNewSearch}>
      ← New search
    </button>
  </div>
)

// ─── Main Page ─────────────────────────────────────────────────────────────

const ResultsPage = () => {
  const urlParams =
    typeof window !== "undefined"
      ? new URLSearchParams(window.location.search)
      : new URLSearchParams()

  const sessionId = urlParams.get("job") || null

  const [status, setStatus] = useState(sessionId ? "polling" : "idle")
  const [results, setResults] = useState(null)
  const [metadata, setMetadata] = useState(null)
  const [error, setError] = useState(null)
  const [elapsed, setElapsed] = useState(0)
  const [newSearchCount, setNewSearchCount] = useState(0)

  const pollRef = useRef(null)
  const timerRef = useRef(null)
  const startTime = useRef(Date.now())

  const searchApiUrl = process.env.GATSBY_SEARCH_API_URL || config.apiUrl

  const stopAll = useCallback(() => {
    if (pollRef.current) clearInterval(pollRef.current)
    if (timerRef.current) clearInterval(timerRef.current)
  }, [])

  const failWithError = useCallback(
    (message, jobId) => {
      stopAll()
      setStatus("error")
      setError({ message, bugUrl: buildBugReportUrl(message, jobId) })
    },
    [stopAll]
  )

  const handleResults = useCallback(
    (data, sid) => {
      stopAll()
      addJobId(data.job_id || sid)
      setResults(
        transformResults(data.results, data.query_length || data.metadata?.query_length)
      )
      setMetadata(
        data.metadata || {
          query_header: data.query_header,
          query_sequence: data.query_sequence,
          query_length: data.query_length,
          num_results: data.num_results,
          database_size: data.database_size,
          search_time_ms: data.search_time_ms,
          timestamp: data.timestamp,
        }
      )
      setStatus("completed")
      setNewSearchCount(n => n + 1)
    },
    [stopAll]
  )

  const poll = useCallback(
    async sid => {
      try {
        const resp = await fetch(`${searchApiUrl}/search/results/${sid}`)
        const data = await resp.json()
        if (!resp.ok) {
          failWithError(data.error || `Server error ${resp.status}`, sid)
          return
        }
        if (data.status === "completed") handleResults(data, sid)
        else if (data.status === "failed") failWithError(data.error || "Search failed", sid)
      } catch {
        failWithError("Failed to fetch results. Please try again.", sid)
      }
    },
    [searchApiUrl, failWithError, handleResults]
  )

  useEffect(() => {
    if (!sessionId) return
    startTime.current = Date.now()
    timerRef.current = setInterval(() => {
      setElapsed(Math.floor((Date.now() - startTime.current) / 1000))
    }, 1000)
    poll(sessionId)
    pollRef.current = setInterval(() => poll(sessionId), 3000)
    return () => stopAll()
  }, [sessionId]) // eslint-disable-line react-hooks/exhaustive-deps

  const handleNewSearch = () => {
    stopAll()
    navigate("/search")
  }

  if (!sessionId) {
    if (typeof window !== "undefined") navigate("/search")
    return null
  }

  return (
    <section className="px-4 py-6">
      <Container>
      {/* Breadcrumb */}
      <nav className="flex items-center gap-1.5 text-sm text-secondary mb-4">
        <Link to="/" className="text-accent hover:text-accent-hover">
          PETadex
        </Link>
        <span className="text-border-strong">/</span>
        <span className='text-secondary-foreground'>Search results</span>
      </nav>

      {/* Main card */}
      <div className="card">
        {status === "polling" && (
          <LoadingScreen sessionId={sessionId} elapsed={elapsed} />
        )}
        {status === "completed" && results && (
          <ResultsView
            results={results}
            metadata={metadata}
            sessionId={sessionId}
            onNewSearch={handleNewSearch}
          />
        )}
        {status === "error" && error && (
          <ErrorView error={error} onNewSearch={handleNewSearch} />
        )}
      </div>

      <SearchHistory
        onSelectSearch={sid => window.open(`/results?job=${sid}`, "_blank")}
        currentJobId={status === "completed" ? sessionId : null}
        newSearchCount={newSearchCount}
      />
      </Container>
    </section>
  )
}

export default ResultsPage
